package model;

public class Contato {
	private String nome;
	private String telefone;
	
	public void setNome(String nome){
		this.nome = nome;
	}
	
	public String getNome(){
		return nome;
	}
	
	public void setTelefone(String fone){
		this.telefone = fone;
	}
	
	public String getTelefone(){
		return telefone;
	}

}
